          <h3>Cyberbullying and Harassment</h3>
          <p>Online harassment and cyberbullying have serious implications for mental health. The UK’s Malicious Communications Act criminalizes sending indecent, grossly offensive, or threatening messages. In the US, various states have specific cyberbullying laws to deter harmful behavior and provide recourse for victims.</p>

          <h3>Child Protection</h3>
          <p>Protecting children online is critical. The Children’s Online Privacy Protection Act (COPPA) in the US regulates the collection of personal information from children under 13, requiring parental consent. The UK’s Children and Young Persons Act protects minors from online exploitation and harmful content.</p>

          <h3>Cybercrime</h3>
          <p>
            Cybercrime laws target illegal online activities like hacking and identity theft. The US’s Computer Fraud and Abuse Act (CFAA) criminalizes unauthorized computer access. The Budapest Convention on Cybercrime is the first international treaty addressing internet crime, harmonizing laws and improving cooperation among nations.
          </p>

